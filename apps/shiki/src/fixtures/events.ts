// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

export function getMockEvents(type?: LiveEventType): LiveEvent[] {
  if (!type) return MOCK_EVENTS;
  return MOCK_EVENTS.filter((e) => e.type === type);
}

export function getMockPinnedEvents(): LiveEvent[] {
  return MOCK_EVENTS.filter((e) => e.pinned);
}

/**
 * Generator that yields events sorted oldest-first (timestamps spread over
 * the last hour). Useful for simulating a live event stream in local-mocked mode.
 */
export function* getMockEventStream(): Generator<LiveEvent, void, unknown> {
  const sorted = [...MOCK_EVENTS].sort(
    (a, b) => new Date(a.timestamp).getTime() - new Date(b.timestamp).getTime(),
  );
  for (const event of sorted) {
    yield event;
  }
}
